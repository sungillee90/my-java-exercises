package fromJava정석;

public class Variable {
    public static void main(String[] args) {
    // There are 8 primitive types which are boolean, char, byte, short, int, long, float, double.

        // instead of hard coding values define variable so that make it dynamic
        System.out.println(6+3);
        System.out.println(6-3);
        System.out.println(6*3);
        System.out.println(6/3);

        int x = 4;
        int y = 2;
        x = 5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

    }
}
